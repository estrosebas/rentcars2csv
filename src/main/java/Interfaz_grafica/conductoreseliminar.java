    void buscar() {
        String username = jTextField1.getText();
        ClienteDAOFactory factory = new ClienteDAOFactoryImpl();
        ClienteDAOInterface cli = factory.createClienteDAO();
        Cliente cliente = cli.buscarClientePorId(username);
        if (cliente != null) {
            jTextField1.setText(cliente.getUser());
            jTextField2.setText(cliente.getContrasena());
            jTextField3.setText(cliente.getNombre());
            jTextField4.setText(cliente.getDireccion());
            String numero = String.valueOf(cliente.getnTelefono());
            jTextField5.setText(numero);
            jTextField6.setText(cliente.getCorreo());
        } else {
            JOptionPane.showMessageDialog(this, "Cliente no encontrado");
        }
    }

    void eliminar() {
        String username = jTextField1.getText();
        ClienteDAOFactory factory = new ClienteDAOFactoryImpl();
        ClienteDAOInterface dao = factory.createClienteDAO();
        boolean exito = dao.eliminar(username);
        if (exito) {
            JOptionPane.showMessageDialog(this, "Cliente eliminado exitosamente");
            // Limpiar los campos de texto
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar el cliente");
        }
    }

